;; **
;;; # OpenCV meets Clojure
;;; 
;;; ## Motivations
;;; 
;;; OpenCV is quite well known in the domain of image manipulation. 
;;; It has all those secret boots you can wear and use to apply filters, resize, rotate, find shapes, remove background etc ...
;;; 
;;; In those days of neural networks, image pre-analysis is actual a fundamental step in preparing a training set.
;;; 
;;; While OpenCV is fantastic to achieve all those tasks, its also quite a pain to re-compile and make the install manually on each machine you want to run it. 
;;; 
;;; This book presents Origami, a simple Clojure wrapper auto generated from the java port of OpenCV.
